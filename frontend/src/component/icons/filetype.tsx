export const FileIcon = (file: File) => {
  const height = '3vh';
  const width = '3vw';
  switch (file.type) {
    case 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet':
      return <IconFiletypeXlsx height={height} width={width} />;
    case 'application/vnd.openxmlformats-officedocument.wordprocessingml.document':
      return <IconFiletypeDocx height={height} width={width} />;
    case 'application/vnd.openxmlformats-officedocument.presentationml.presentation':
      return <IconFiletypePptx height={height} width={width} />;
    case 'application/pdf':
      return <IconFiletypePdf height={height} width={width} />;
    case 'text/plain':
      return <IconFiletypeTxt height={height} width={width} />;
    case 'applicaiton/zip':
      return <IconFileEarmarkZip height={height} width={width} />;
    case 'image/png':
      return <IconFiletypePng height={height} width={width} />;
    case 'image/gif':
      return <IconFiletypeGif height={height} width={width} />;
    case 'image/jpeg':
      return <IconFiletypeJpg height={height} width={width} />;
    case 'video/mpeg':
      return <IconFiletypeMp4 height={height} width={width} />;
    case 'text/csv':
      return <IconFiletypeCsv height={height} width={width} />;
    default:
      return <IconFile height={height} width={width} />;
  }
};
